def compute_metrics(y_true, y_prob, threshold=0.5):
    y_pred = (y_prob >= threshold).astype(int)
    out = {
        "accuracy": float(accuracy_score(y_true, y_pred)),
        "precision": float(precision_score(y_true, y_pred, zero_division=0)),
        "recall": float(recall_score(y_true, y_pred, zero_division=0)),
        "f1": float(f1_score(y_true, y_pred, zero_division=0)),
        "roc_auc": float(roc_auc_score(y_true, y_prob)) if len(np.unique(y_true)) > 1 else np.nan,
        "mcc": float(matthews_corrcoef(y_true, y_pred)) if len(np.unique(y_pred)) > 1 else np.nan,
        "balanced_accuracy": float(balanced_accuracy_score(y_true, y_pred)),
        "threshold": float(threshold),
    }
    return out, y_pred

def optimise_threshold(y_true, y_prob):
    best_thresh, best_f1 = 0.5, 0.0
    for t in np.linspace(0.2, 0.8, 121):
        preds = (y_prob >= t).astype(int)
        f = f1_score(y_true, preds, zero_division=0)
        if f > best_f1:
            best_f1, best_thresh = f, t
    return float(best_thresh)

def bootstrap_ci(y_true, y_prob, metric_name="roc_auc",
                 rounds=BOOTSTRAP_ROUNDS, seed=42):
    rng, values, n = np.random.default_rng(seed), [], len(y_true)
    for _ in range(rounds):
        idx = rng.integers(0, n, size=n)
        y_b, p_b = y_true[idx], y_prob[idx]
        if metric_name == "roc_auc":
            if len(np.unique(y_b)) < 2:
                continue
            values.append(roc_auc_score(y_b, p_b))
        else:
            preds = (p_b >= 0.5).astype(int)
            fn = {
                "accuracy": accuracy_score,
                "f1": lambda a, b: f1_score(a, b, zero_division=0),
                "mcc": matthews_corrcoef,
                "balanced_accuracy": balanced_accuracy_score
            }.get(metric_name)
            if fn is None:
                continue
            if metric_name == "mcc" and len(np.unique(preds)) < 2:
                continue
            values.append(fn(y_b, preds))
    if not values:
        return {"mean": np.nan, "ci_low": np.nan, "ci_high": np.nan}
    return {
        "mean": float(np.mean(values)),
        "ci_low": float(np.percentile(values, 2.5)),
        "ci_high": float(np.percentile(values, 97.5)),
    }

def detect_low_sample_regime(n_train, threshold=LOW_SAMPLE_THRESHOLD):
    if n_train >= threshold:
        return "normal", None
    elif n_train >= 100:
        warning = (
            f"⚠ LOW-SAMPLE TARGET (n_train={n_train}). "
            f"Standard k-fold CV replaced by stratified bootstrap CV "
            f"({BOOTSTRAP_ROUNDS_SMALL} rounds). "
            f"Performance metrics may be unstable; treat with caution. "
            f"Results with AUC ≥ 0.95 may reflect data artefacts rather than "
            f"genuine model generalisation."
        )
        return "bootstrap", warning
    else:
        warning = (
            f"⚠ VERY LOW-SAMPLE TARGET (n_train={n_train}). "
            f"LOO-CV applied. Reported metrics are highly sensitive to "
            f"individual data points. Do NOT over-interpret near-perfect accuracy."
        )
        return "loo", warning

def stratified_bootstrap_cv(model_fn, scaler_fn, X, y,
                            rounds=BOOTSTRAP_ROUNDS_SMALL, seed=42):
    rng, rows = np.random.default_rng(seed), []
    n = len(y)
    for r in range(rounds):
        idx_tr = rng.integers(0, n, size=n)
        idx_va = np.setdiff1d(np.arange(n), idx_tr)
        if len(idx_va) == 0 or len(np.unique(y[idx_tr])) < 2:
            continue

        scaler = scaler_fn()
        X_tr_s = scaler.fit_transform(X[idx_tr])
        X_va_s = scaler.transform(X[idx_va])

        model = model_fn()
        model.fit(X_tr_s, y[idx_tr])

        y_prob = (model.predict_proba(X_va_s)[:, 1]
                  if hasattr(model, "predict_proba")
                  else model.predict(X_va_s))
        if len(np.unique(y[idx_va])) < 2:
            continue
        m, _ = compute_metrics(y[idx_va], y_prob)
        m["round"] = r
        rows.append(m)
    return rows

def loo_cv(model_fn, scaler_fn, X, y):
    loo = LeaveOneOut()
    rows = []
    y_true_all, y_prob_all = [], []
    for tr_idx, va_idx in loo.split(X):
        if len(np.unique(y[tr_idx])) < 2:
            continue
        scaler = scaler_fn()
        X_tr_s = scaler.fit_transform(X[tr_idx])
        X_va_s = scaler.transform(X[va_idx])
        model = model_fn()
        model.fit(X_tr_s, y[tr_idx])
        y_prob = (model.predict_proba(X_va_s)[:, 1]
                  if hasattr(model, "predict_proba")
                  else model.predict(X_va_s))
        y_true_all.append(y[va_idx[0]])
        y_prob_all.append(float(y_prob[0]))

    y_true_all = np.array(y_true_all)
    y_prob_all = np.array(y_prob_all)
    if len(np.unique(y_true_all)) > 1:
        m, _ = compute_metrics(y_true_all, y_prob_all)
        return [m]
    return []

