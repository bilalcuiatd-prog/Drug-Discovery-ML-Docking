def fragment_to_pharmacophore(frag_smiles):
    if not frag_smiles:
        return ""
    frag_mol = Chem.MolFromSmiles(frag_smiles)
    if frag_mol is None:
        return ""
    labels = [name for name, pat in _PHARM_MOLS if frag_mol.HasSubstructMatch(pat)]
    return "; ".join(labels)

