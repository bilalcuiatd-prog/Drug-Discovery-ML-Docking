def update_cross_target_shap(shap_df, target_name, top_k=20):
    registry_path = os.path.join(OUTPUT_DIR, "_shap_registry.json")

    if os.path.exists(registry_path):
        with open(registry_path) as f:
            registry = json.load(f)
    else:
        registry = {}

    if shap_df is not None:
        registry[target_name] = shap_df.head(top_k)["feature_name"].tolist()
    else:
        registry[target_name] = []

    with open(registry_path, "w") as f:
        json.dump(registry, f, indent=2)

    if len(registry) > 1:
        targets = list(registry.keys())
        mat = np.zeros((len(targets), len(targets)))
        for i, t1 in enumerate(targets):
            s1 = set(registry[t1])
            for j, t2 in enumerate(targets):
                s2 = set(registry[t2])
                den = len(s1 | s2)
                mat[i, j] = len(s1 & s2) / den if den > 0 else 0.0
        jac_df = pd.DataFrame(mat, index=targets, columns=targets)
        jac_df.to_csv(SHAP_CROSS_TARGET_PATH)
        print(f"\nCross-target SHAP Jaccard matrix saved → {SHAP_CROSS_TARGET_PATH}")
    return registry

