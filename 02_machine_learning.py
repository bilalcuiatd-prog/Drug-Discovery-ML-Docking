def residual_block(x, units, dropout_rate, l2_reg=1e-4):
    shortcut = x
    h = Dense(units, kernel_regularizer=l2(l2_reg))(x)
    h = LeakyReLU(negative_slope=0.1)(h)
    h = BatchNormalization()(h)
    h = Dropout(dropout_rate)(h)
    if K.int_shape(shortcut)[-1] != units:
        shortcut = Dense(units, use_bias=False, kernel_regularizer=l2(l2_reg))(shortcut)
    return Add()([shortcut, h])

def build_residual_dnn(input_dim):
    inputs = Input(shape=(input_dim,))
    x = Dense(512, kernel_regularizer=l2(1e-4))(inputs)
    x = LeakyReLU(negative_slope=0.1)(x)
    x = BatchNormalization()(x)
    x = Dropout(0.30)(x)
    for units, dr in [(512, 0.30), (512, 0.30), (256, 0.25), (256, 0.25), (128, 0.20)]:
        x = residual_block(x, units, dr)
    x = Dense(64, kernel_regularizer=l2(1e-4))(x)
    x = LeakyReLU(negative_slope=0.1)(x)
    x = BatchNormalization()(x)
    x = Dropout(0.15)(x)
    outputs = Dense(1, activation="sigmoid")(x)
    model = Model(inputs, outputs)
    model.compile(
        optimizer=AdamW(learning_rate=LEARNING_RATE, weight_decay=1e-5),
        loss=focal_loss(FOCAL_GAMMA, FOCAL_ALPHA),
        metrics=[
            tf.keras.metrics.BinaryAccuracy(name="accuracy"),
            tf.keras.metrics.AUC(name="auc"),
        ],
    )
    return model

def cosine_annealing_schedule(epoch, total=DNN_EPOCHS,
                              lr_min=1e-6, lr_max=LEARNING_RATE):
    return float(
        lr_min + 0.5 * (lr_max - lr_min) *
        (1 + math.cos(math.pi * (epoch % total) / total))
    )

