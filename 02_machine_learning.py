def build_feature_selector(X_train, y_train,
                           var_threshold=VARIANCE_THRESHOLD,
                           top_k=TOP_K_FEATURES):
    vt = VarianceThreshold(threshold=var_threshold)
    vt.fit(X_train)
    mask_var = vt.get_support()

    if top_k is None or top_k >= mask_var.sum():
        return mask_var

    X_sub = X_train[:, mask_var]
    mi = mutual_info_classif(X_sub, y_train, random_state=RANDOM_STATE)
    keep_local = np.argsort(mi)[::-1][:top_k]

    orig_indices = np.where(mask_var)[0]
    final_mask = np.zeros(X_train.shape[1], dtype=bool)
    final_mask[orig_indices[keep_local]] = True
    return final_mask

