def prepare_dataset(csv_path, smiles_col="canonical_smiles", label_col="class"):
    df = pd.read_csv(csv_path).copy()
    if smiles_col not in df.columns:
        raise ValueError(f"Missing column: {smiles_col}")
    if label_col not in df.columns:
        raise ValueError(f"Missing column: {label_col}")

    df = df[[smiles_col, label_col]].rename(
        columns={smiles_col: "smiles_raw", label_col: "label_raw"}
    )
    df["label_raw"] = df["label_raw"].astype(str).str.strip().str.lower()
    df = df[df["label_raw"].isin(["active", "inactive"])].copy()

    df["canonical_smiles"] = df["smiles_raw"].apply(canonicalize_smiles)
    df = df.dropna(subset=["canonical_smiles"]).drop_duplicates(
        subset=["canonical_smiles"]
    ).reset_index(drop=True)

    mols, scaffolds, morgan_arrays, bit_infos, maccs_arrays, desc_dicts = (
        [], [], [], [], [], []
    )
    for smi in df["canonical_smiles"]:
        mol = smiles_to_clean_mol(smi)
        mols.append(mol)
        scaffolds.append(get_scaffold_smiles(mol) if mol else None)

        if mol is None:
            for lst in [morgan_arrays, bit_infos, maccs_arrays, desc_dicts]:
                lst.append(None)
            continue

        mbits, binfo = morgan_bits_with_info(mol, nbits=MORGAN_BITS, radius=MORGAN_RADIUS)
        morgan_arrays.append(mbits)
        bit_infos.append(binfo)
        maccs_arrays.append(maccs_bits(mol) if USE_MACCS else None)
        desc_dicts.append(calc_rdkit_descriptors(mol) if USE_RDKIT_DESCRIPTORS else None)

    df["mol"]         = mols
    df["scaffold"]    = scaffolds
    df["morgan_bits"] = morgan_arrays
    df["bit_info"]    = bit_infos
    df["maccs_bits"]  = maccs_arrays
    df["desc_dict"]   = desc_dicts
    df = df.dropna(subset=["morgan_bits"]).reset_index(drop=True)

    y = df["label_raw"].map({"inactive": 0, "active": 1}).astype(int).values

    feature_blocks, feature_names = [], []

    X_morgan = np.vstack(df["morgan_bits"].values).astype(np.float32)
    feature_blocks.append(X_morgan)
    feature_names += [f"ECFP4_{i}" for i in range(X_morgan.shape[1])]

    if USE_MACCS:
        X_maccs = np.vstack(df["maccs_bits"].values).astype(np.float32)
        feature_blocks.append(X_maccs)
        feature_names += [f"MACCS_{i}" for i in range(X_maccs.shape[1])]

    if USE_RDKIT_DESCRIPTORS:
        desc_df = pd.DataFrame(df["desc_dict"].tolist())
        desc_df.replace([np.inf, -np.inf], np.nan, inplace=True)
        desc_df.fillna(desc_df.median(), inplace=True)
        X_desc = desc_df.values.astype(np.float32)
        feature_blocks.append(X_desc)
        feature_names += list(desc_df.columns)

    X = np.hstack(feature_blocks).astype(np.float32)
    X = np.nan_to_num(X, nan=0.0, posinf=0.0, neginf=0.0)

    feature_map = pd.DataFrame({
        "feature_index": np.arange(len(feature_names)),
        "feature_name": feature_names,
    })
    return df, X, y, feature_map

