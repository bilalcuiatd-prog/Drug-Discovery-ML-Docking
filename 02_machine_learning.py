def scaffold_split(df, test_fraction=0.2, seed=42):
    rng = np.random.default_rng(seed)
    scaffold_to_indices = {}
    for idx, scaf in enumerate(df["scaffold"].fillna("NO_SCAFFOLD")):
        scaffold_to_indices.setdefault(scaf, []).append(idx)

    scaffold_groups = list(scaffold_to_indices.items())
    rng.shuffle(scaffold_groups)

    total_n = len(df)
    target_test_n = int(math.ceil(total_n * test_fraction))
    test_indices, train_indices, running = [], [], 0

    for _, idxs in scaffold_groups:
        if running < target_test_n:
            test_indices.extend(idxs)
            running += len(idxs)
        else:
            train_indices.extend(idxs)

    if not train_indices or not test_indices:
        all_idx = np.arange(total_n)
        train_indices, test_indices = train_test_split(
            all_idx, test_size=test_fraction, random_state=seed,
            stratify=df["label_raw"].map({"inactive": 0, "active": 1})
        )
    return np.array(train_indices), np.array(test_indices)

