def evaluate_baselines_cv(X, y, seed=42, cv_strategy="normal"):
    rows = []

    for model_name, model in get_baseline_models(seed).items():

        if cv_strategy == "loo":
            fold_rows = loo_cv(
                model_fn=lambda: get_baseline_models(seed)[model_name],
                scaler_fn=RobustScaler,
                X=X, y=y
            )
            for m in fold_rows:
                m["model"] = model_name
                m["fold"] = 0
                rows.append(m)

        elif cv_strategy == "bootstrap":
            fold_rows = stratified_bootstrap_cv(
                model_fn=lambda: get_baseline_models(seed)[model_name],
                scaler_fn=RobustScaler,
                X=X, y=y
            )
            for m in fold_rows:
                m["model"] = model_name
                rows.append(m)

        else:
            cv = RepeatedStratifiedKFold(
                n_splits=N_SPLITS, n_repeats=N_REPEATS, random_state=seed
            )
            for fold_id, (tr_idx, va_idx) in enumerate(cv.split(X, y), 1):
                scaler = RobustScaler()
                X_tr_s = scaler.fit_transform(X[tr_idx])
                X_va_s = scaler.transform(X[va_idx])
                model.fit(X_tr_s, y[tr_idx])
                y_prob = (
                    model.predict_proba(X_va_s)[:, 1]
                    if hasattr(model, "predict_proba")
                    else model.predict(X_va_s)
                )
                m, _ = compute_metrics(y[va_idx], y_prob)
                m.update({"model": model_name, "fold": fold_id})
                rows.append(m)

    return pd.DataFrame(rows)

def evaluate_dnn_cv(X, y, seed=42, cv_strategy="normal"):
    rows, histories = [], []

    callbacks_fn = lambda: [
        EarlyStopping(monitor="val_loss", patience=PATIENCE,
                      restore_best_weights=True, verbose=0),
        LearningRateScheduler(
            lambda ep: cosine_annealing_schedule(ep, DNN_EPOCHS), verbose=0
        ),
    ]

    if cv_strategy in ("loo", "bootstrap"):
        X_tr, X_va, y_tr, y_va = train_test_split(
            X, y,
            test_size=max(0.2, 1.0 / max(len(y), 2)),
            random_state=seed,
            stratify=y if len(np.unique(y)) > 1 else None
        )
        scaler = RobustScaler()
        X_tr_s = scaler.fit_transform(X_tr)
        X_va_s = scaler.transform(X_va)
        seed_everything(seed)
        m_dnn = build_residual_dnn(X_tr_s.shape[1])
        n_neg = int((y_tr == 0).sum())
        n_pos = int((y_tr == 1).sum())
        cw = {0: 1.0, 1: float(n_neg / (n_pos + 1e-8))}
        hist = m_dnn.fit(
            X_tr_s, y_tr, validation_data=(X_va_s, y_va),
            epochs=DNN_EPOCHS, batch_size=DNN_BATCH_SIZE,
            class_weight=cw, verbose=0, callbacks=callbacks_fn(),
        )
        y_prob = m_dnn.predict(X_va_s, verbose=0).ravel()
        m, _ = compute_metrics(y_va, y_prob)
        m.update({
            "model": "TensorFlowDNN_v3",
            "fold": 0,
            "epochs_trained": len(hist.history["loss"])
        })
        rows.append(m)
        histories.append(hist.history)

    else:
        cv = RepeatedStratifiedKFold(
            n_splits=N_SPLITS, n_repeats=N_REPEATS, random_state=seed
        )
        for fold_id, (tr_idx, va_idx) in enumerate(cv.split(X, y), 1):
            seed_everything(seed + fold_id)
            scaler = RobustScaler()
            X_tr_s = scaler.fit_transform(X[tr_idx])
            X_va_s = scaler.transform(X[va_idx])
            n_neg = int((y[tr_idx] == 0).sum())
            n_pos = int((y[tr_idx] == 1).sum())
            cw = {0: 1.0, 1: float(n_neg / (n_pos + 1e-8))}
            m_dnn = build_residual_dnn(X_tr_s.shape[1])
            hist = m_dnn.fit(
                X_tr_s, y[tr_idx],
                validation_data=(X_va_s, y[va_idx]),
                epochs=DNN_EPOCHS, batch_size=DNN_BATCH_SIZE,
                class_weight=cw, verbose=0, callbacks=callbacks_fn(),
            )
            y_prob = m_dnn.predict(X_va_s, verbose=0).ravel()
            m, _ = compute_metrics(y[va_idx], y_prob)
            m.update({
                "model": "TensorFlowDNN_v3",
                "fold": fold_id,
                "epochs_trained": len(hist.history["loss"])
            })
            rows.append(m)
            histories.append(hist.history)

    return pd.DataFrame(rows), histories

