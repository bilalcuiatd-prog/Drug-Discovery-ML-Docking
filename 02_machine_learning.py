def keep_largest_fragment(mol):
    if mol is None:
        return None
    frags = Chem.GetMolFrags(mol, asMols=True, sanitizeFrags=True)
    return max(frags, key=lambda m: m.GetNumHeavyAtoms()) if frags else None

def smiles_to_clean_mol(smiles):
    if pd.isna(smiles):
        return None
    mol = Chem.MolFromSmiles(str(smiles))
    if mol is None:
        return None
    mol = keep_largest_fragment(mol)
    if mol is None:
        return None
    try:
        Chem.SanitizeMol(mol)
    except Exception:
        return None
    return mol

def canonicalize_smiles(smiles):
    mol = smiles_to_clean_mol(smiles)
    return Chem.MolToSmiles(mol, canonical=True) if mol else None

_RDKIT_DESC_NAMES = [
    d[0] for d in Descriptors.descList if not d[0].startswith("Ipc")
]
_CALC = MoleculeDescriptors.MolecularDescriptorCalculator(_RDKIT_DESC_NAMES)

def calc_rdkit_descriptors(mol):
    try:
        vals = _CALC.CalcDescriptors(mol)
    except Exception:
        vals = [np.nan] * len(_RDKIT_DESC_NAMES)
    return dict(zip(_RDKIT_DESC_NAMES, vals))

def morgan_bits_with_info(mol, nbits=2048, radius=2):
    bit_info = {}
    fp = AllChem.GetMorganFingerprintAsBitVect(
        mol, radius=radius, nBits=nbits, bitInfo=bit_info
    )
    arr = np.zeros((nbits,), dtype=np.int8)
    DataStructs.ConvertToNumpyArray(fp, arr)
    return arr, bit_info

def maccs_bits(mol):
    fp = AllChem.GetMACCSKeysFingerprint(mol)
    arr = np.zeros((fp.GetNumBits(),), dtype=np.int8)
    DataStructs.ConvertToNumpyArray(fp, arr)
    return arr

def get_scaffold_smiles(mol):
    try:
        return MurckoScaffold.MurckoScaffoldSmiles(mol=mol)
    except Exception:
        return None

def bit_to_substructures(mol, bit_id, bit_info):
    substructures = set()
    if bit_id not in bit_info:
        return []
    for atom_idx, radius in bit_info[bit_id]:
        env = Chem.FindAtomEnvironmentOfRadiusN(mol, radius, atom_idx)
        atoms = {atom_idx}
        for bond_id in env:
            bond = mol.GetBondWithIdx(bond_id)
            atoms.add(bond.GetBeginAtomIdx())
            atoms.add(bond.GetEndAtomIdx())
        try:
            frag_smiles = Chem.MolFragmentToSmiles(
                mol, atomsToUse=sorted(atoms),
                bondsToUse=list(env), canonical=True
            )
            if frag_smiles:
                substructures.add(frag_smiles)
        except Exception:
            continue
    return sorted(substructures)[:5]

