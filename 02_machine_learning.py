def train_ensemble(X_tr_s, y_tr, X_val_s, y_val,
                   n_models=ENSEMBLE_SIZE, base_seed=RANDOM_STATE):
    models = []
    for i in range(n_models):
        seed_everything(base_seed + i * 7)
        m = build_residual_dnn(X_tr_s.shape[1])
        n_neg = int((y_tr == 0).sum())
        n_pos = int((y_tr == 1).sum())
        cw = {0: 1.0, 1: float(n_neg / (n_pos + 1e-8))}
        ckpt = os.path.join(OUTPUT_DIR, f"ensemble_ckpt_{i}.keras")
        m.fit(
            X_tr_s, y_tr, validation_data=(X_val_s, y_val),
            epochs=DNN_EPOCHS, batch_size=DNN_BATCH_SIZE,
            class_weight=cw, verbose=1,
            callbacks=[
                EarlyStopping(monitor="val_loss", patience=PATIENCE,
                              restore_best_weights=True, verbose=0),
                ModelCheckpoint(ckpt, monitor="val_loss",
                                save_best_only=True, verbose=0),
                LearningRateScheduler(
                    lambda ep: cosine_annealing_schedule(ep, DNN_EPOCHS),
                    verbose=0
                ),
            ],
        )
        m = load_model(
            ckpt,
            custom_objects={"_loss": focal_loss(FOCAL_GAMMA, FOCAL_ALPHA)}
        )
        models.append(m)
        print(f"  Ensemble member {i+1}/{n_models} done.")
    return models

def ensemble_predict(models, X_scaled):
    probs = np.stack([m.predict(X_scaled, verbose=0).ravel() for m in models])
    return probs.mean(axis=0)

