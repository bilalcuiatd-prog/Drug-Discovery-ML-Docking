def explain_with_shap(
    models, scaler, X_background, X_explain,
    feature_names, df_source, output_csv,
    max_background=100, max_explain=50,
    target_name="target",
):
    if not SHAP_AVAILABLE:
        print("SHAP not installed; skipping.")
        return None

    bg_idx = np.random.choice(len(X_background),
                              min(max_background, len(X_background)), replace=False)
    ex_idx = np.random.choice(len(X_explain),
                              min(max_explain, len(X_explain)), replace=False)

    X_bg_s = scaler.transform(X_background[bg_idx])
    X_ex_s = scaler.transform(X_explain[ex_idx])

    def predict_fn(x):
        return ensemble_predict(models, x)

    explainer = shap.KernelExplainer(predict_fn, X_bg_s)
    shap_values = explainer.shap_values(X_ex_s, nsamples=200)
    if isinstance(shap_values, list):
        shap_values = shap_values[0]

    mean_abs = np.abs(shap_values).mean(axis=0)
    shap_df = pd.DataFrame({
        "feature_index": np.arange(len(feature_names)),
        "feature_name": feature_names,
        "mean_abs_shap": mean_abs,
    }).sort_values("mean_abs_shap", ascending=False)

    explain_src = df_source.iloc[ex_idx].reset_index(drop=True)
    mapped_rows = []

    for _, row in shap_df.head(SHAP_TOP_K).iterrows():
        fname = row["feature_name"]

        if fname.startswith("ECFP4_"):
            desc_type = "Morgan ECFP4 fingerprint"
        elif fname.startswith("MACCS_"):
            desc_type = "MACCS key"
        else:
            desc_type = "RDKit physicochemical descriptor"

        frag_smiles_examples = ""
        pharm_categories = ""

        if fname.startswith("ECFP4_"):
            bit_id = int(fname.split("_")[1])
            examples = set()
            for i in range(len(explain_src)):
                bit_info = explain_src.loc[i, "bit_info"]
                mol = explain_src.loc[i, "mol"]
                bit_vector = explain_src.loc[i, "morgan_bits"]
                if mol is None or bit_info is None:
                    continue
                if bit_id >= len(bit_vector):
                    continue
                if int(bit_vector[bit_id]) != 1:
                    continue
                for frag in bit_to_substructures(mol, bit_id, bit_info):
                    examples.add(frag)
                if len(examples) >= 5:
                    break

            frag_smiles_examples = " | ".join(sorted(examples))

            all_pharm = set()
            for frag in sorted(examples):
                for ph in fragment_to_pharmacophore(frag).split("; "):
                    if ph:
                        all_pharm.add(ph)
            pharm_categories = "; ".join(sorted(all_pharm))

        elif fname.startswith("MACCS_"):
            maccs_bit = int(fname.split("_")[1])
            _MACCS_LABELS = {
                1: "isotope", 2: "aromatic ring", 3: "heterocyclic",
                5: "ring", 7: "nitrogen heterocycle",
                8: "ring size 4", 9: "ring size 5", 10: "ring size 6",
                11: "C-C=O", 12: "nitro group", 14: "CC=O non-cyclic",
                15: "halogen", 16: "amide", 17: "C-S", 18: "C-N",
                19: "aromatic NH", 20: "ether oxygen", 21: "aldehyde",
                22: "ketone", 24: "ester", 25: "carboxylic acid",
                29: "epoxide", 30: "nitrogen", 31: "primary amine",
                32: "hydroxyl", 33: "sulfur", 35: "amide bond",
            }
            pharm_categories = _MACCS_LABELS.get(maccs_bit, f"MACCS bit {maccs_bit}")

        mapped_rows.append({
            "feature_index": int(row["feature_index"]),
            "feature_name": fname,
            "descriptor_type": desc_type,
            "mean_abs_shap": float(row["mean_abs_shap"]),
            "fragment_smiles": frag_smiles_examples,
            "pharmacophore_categories": pharm_categories,
        })

    out_df = pd.DataFrame(mapped_rows)
    out_df.to_csv(output_csv, index=False)
    return out_df

