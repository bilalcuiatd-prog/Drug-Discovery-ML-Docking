def keep_largest_fragment(mol):
    frags = Chem.GetMolFrags(mol, asMols=True, sanitizeFrags=True)
    if len(frags) == 0:
        return None
    return max(frags, key=lambda m: m.GetNumHeavyAtoms())


def get_alerts(mol, catalog):
    try:
        matches = catalog.GetMatches(mol)
        desc = [m.GetDescription() for m in matches]
        return len(desc), "; ".join(desc)
    except Exception:
        return 0, ""


def admet_descriptor_row(smiles):
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return pd.Series([np.nan] * 24)

    mol = keep_largest_fragment(mol)
    if mol is None:
        return pd.Series([np.nan] * 24)

    mw = Descriptors.MolWt(mol)
    logp = Crippen.MolLogP(mol)
    hbd = Lipinski.NumHDonors(mol)
    hba = Lipinski.NumHAcceptors(mol)
    tpsa = Descriptors.TPSA(mol)
    rotb = Lipinski.NumRotatableBonds(mol)
    rings = rdMolDescriptors.CalcNumRings(mol)
    arom_rings = rdMolDescriptors.CalcNumAromaticRings(mol)
    heavy_atoms = mol.GetNumHeavyAtoms()
    frac_csp3 = rdMolDescriptors.CalcFractionCSP3(mol)
    mr = Crippen.MolMR(mol)
    qed = QED.qed(mol)

    pains_n, pains_desc = get_alerts(mol, PAINS_CATALOG)
    brenk_n, brenk_desc = get_alerts(mol, BRENK_CATALOG)

    # Lipinski: usually acceptable if 0 or 1 violation
    lipinski_viol = int(mw > 500) + int(logp > 5) + int(hbd > 5) + int(hba > 10)
    lipinski_pass = int(lipinski_viol <= 1)

    veber_pass = int((rotb <= 10) and (tpsa <= 140))
    egan_pass = int((logp <= 5.88) and (tpsa <= 131.6))
    ghose_pass = int((160 <= mw <= 480) and (-0.4 <= logp <= 5.6) and (20 <= heavy_atoms <= 70) and (40 <= mr <= 130))
    muegge_pass = int((200 <= mw <= 600) and (-2 <= logp <= 5) and (tpsa <= 150) and (hbd <= 5) and (hba <= 10) and (rotb <= 15) and (rings >= 1))

    # Simple CNS/BBB-friendly heuristic
    # This is only a rough screening rule, not a true BBB model
    bbb_like = int((mw <= 450) and (1 <= logp <= 5) and (tpsa <= 90) and (hbd <= 2) and (rotb <= 8))

    no_pains = int(pains_n == 0)
    no_brenk = int(brenk_n == 0)

    # Simple internal prioritization score
    admet_score = (
        lipinski_pass +
        veber_pass +
        egan_pass +
        ghose_pass +
        muegge_pass +
        no_pains +
        no_brenk +
        bbb_like +
        float(qed)
    )

    return pd.Series([
        mw, logp, hbd, hba, tpsa, rotb,
        rings, arom_rings, heavy_atoms, frac_csp3, mr, qed,
        lipinski_viol, lipinski_pass, veber_pass, egan_pass, ghose_pass, muegge_pass,
        pains_n, pains_desc, brenk_n, brenk_desc,
        bbb_like, admet_score
    ])

