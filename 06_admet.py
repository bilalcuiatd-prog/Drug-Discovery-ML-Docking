def admet_comment(row):
    notes = []

    if row["BBB_like"] == 1:
        notes.append("CNS-friendly heuristic: yes")
    else:
        notes.append("CNS-friendly heuristic: no")

    if row["Lipinski_pass"] == 1 and row["Veber_pass"] == 1:
        notes.append("oral-likeness: favorable")
    else:
        notes.append("oral-likeness: limited")

    if row["PAINS_alerts"] > 0:
        notes.append("PAINS alert present")
    if row["Brenk_alerts"] > 0:
        notes.append("Brenk alert present")

    if row["TPSA"] > 90:
        notes.append("high TPSA may limit BBB")
    if row["LogP"] > 5:
        notes.append("high lipophilicity")
    if row["MW"] > 500:
        notes.append("high molecular weight")

    return " | ".join(notes)

