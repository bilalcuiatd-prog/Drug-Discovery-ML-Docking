import numpy as np
import pandas as pd

def compute_contacts(protein_df, ligand_df, cutoff=4.0):
    contacts = []
    lig_xyz = ligand_df[["x", "y", "z"]].values
    prot_xyz = protein_df[["x", "y", "z"]].values

    for i, p in protein_df.iterrows():
        dists = np.sqrt(((lig_xyz - prot_xyz[i]) ** 2).sum(axis=1))
        min_dist = dists.min()
        if min_dist <= cutoff:
            contacts.append({
                "residue": f"{p['res_name']}{p['res_id']}",
                "chain": p["chain_id"],
                "atom_name": p["atom_name"],
                "min_distance": min_dist
            })
    contact_df = pd.DataFrame(contacts)
    if len(contact_df) == 0:
        return contact_df
    contact_df = contact_df.sort_values("min_distance")
    return contact_df

